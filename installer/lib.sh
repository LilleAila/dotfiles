#!/usr/bin/env bash
function nixf() {
  nix --extra-experimental-features "nix-command-flakes" "$@"
}

function yesno() {
    local prompt="$1"

    while true; do
        read -rp "$prompt [y/n] " yn
        case $yn in
            [Yy]* ) echo "y"; return;;
            [Nn]* ) echo "n"; return;;
            * ) echo "Please answer yes or no.";;
        esac
    done
}
